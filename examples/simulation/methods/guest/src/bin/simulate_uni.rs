use alloy_primitives::{Address, U256,U160};
use alloy_primitives::aliases::U24;
use alloy_sol_types::{sol, SolValue};
use risc0_steel::{
    ethereum::{EthEvmInput, ETH_MAINNET_CHAIN_SPEC},
    Commitment, Contract,
};
use risc0_zkvm::guest::env;

risc0_zkvm::guest::entry!(main);

/// Specify the function to call using the [`sol!`] macro.
/// This parses the Solidity syntax to generate a struct that implements the `SolCall` trait.
sol! {
    /// ERC-20 balance function signature.
    interface IERC20 {
        function balanceOf(address account) external view returns (uint);
    }
    interface IQuoter {
        function quoteExactInputSingle(
            address tokenIn,
            address tokenOut,
            uint24 fee,
            uint256 amountIn,
            uint160 sqrtPriceLimitX96
        ) external returns (uint256 amountOut);

        function factory() returns (address);
    }
}

/// ABI encodable journal data.
sol! {
    struct Journal {
        Commitment commitment;
        // address fromAddress;
        address tokenIn;
        // address tokenOut;
        // uint256 price;

    }
}

fn main() {
    // Read the input from the guest environment.
    let input: EthEvmInput = env::read();
    let contract: Address = env::read();
    let token_in:Address =env::read();
    let token_out:Address =env::read();
    let from_address:Address =env::read();


    
    let account: Address = env::read();

    // Converts the input into a `EvmEnv` for execution. The `with_chain_spec` method is used
    // to specify the chain configuration. It checks that the state matches the state root in the
    // header provided in the input.
     let env = input.into_env().with_chain_spec(&ETH_MAINNET_CHAIN_SPEC);


    // Execute the view call; it returns the result in the type generated by the `sol!` macro.
    // let call = IERC20::balanceOfCall { account };
    // let returns = Contract::new(contract, &env).call_builder(&call).call();


    // let token_in = Address::parse_checksummed("0xA0b86991c6218b36c1d19D4a2e9Eb0cE3606eB48", None).unwrap();
    // let token_out = Address::parse_checksummed("0xC02aaA39b223FE8D0A0e5C4F27eAD9083C756Cc2", None).unwrap();
    // let from_address = Address::parse_checksummed("0xD6153F5af5679a75cC85D8974463545181f48772", None).unwrap();


    // Prepare the function call
    let quote_call = IQuoter::quoteExactInputSingleCall {
        tokenIn: token_in, tokenOut: token_out,fee: U24::from(3000),amountIn: U256::from(1000),sqrtPriceLimitX96: U160::from(0)
    };

    let mut ct = Contract::new(contract, &env);
    let returns = ct.call_builder(&quote_call).from(from_address).call();

    

    println!("Returns:------------------------------ {}", returns.amountOut);



    // Check that the given account holds at least 1 token.
 
    // Commit the block hash and number used when deriving `view_call_env` to the journal.
    let journal = Journal {
        commitment: env.into_commitment(),
        // fromAddress: from_address,
        tokenIn: token_in,
        // tokenOut: token_out,
        // price:returns.amountOut

    };
    env::commit_slice(&journal.abi_encode());
}


 